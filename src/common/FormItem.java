package common;

public class FormItem {
	
	public String type;
	public String label;
	public String namespace;
	public boolean optional;
	public String property;
	public boolean ukda;
	public String resourceNamespace;
	public String resourceName;
	
	public FormItem()
	{
		
	}
	
	public String getResourceNamespace()
	{
		return resourceNamespace;
	}
	
	public String getResourceName()
	{
		return resourceName;
	}
	
	public String getType()
	{
		return type;
	}
	
	public String getLabel()
	{
		return label;
	}
	
	public String getNamespace()
	{
		return namespace;
	}
	
	public String getProperty()
	{
		return property;
	}
	
	public boolean getOptional()
	{
		return optional;
	}
	
	public boolean getUKDA()
	{
		return ukda;
	}
	
	public void setType(String type)
	{
		this.type = type;
	}
	
	public void setLabel(String label)
	{
		this.label = label;
	}
	
	public void setNamespace(String namespace)
	{
		this.namespace = namespace;
	}
	
	public void setResourceNamespace(String resourceNamespace)
	{
		this.resourceNamespace = resourceNamespace;
	}
	
	public void setResourceName(String resourceName)
	{
		this.resourceName = resourceName;
	}
	
	public void setProperty(String property)
	{
		this.property = property;
	}
	
	public void setOptionalTrue()
	{
		optional = true;
	}
	
	public void setOptionalFalse()
	{
		optional = false;
	}
	
	public void setUKDATrue()
	{
		ukda = true;
	}
	
	public void setUKDAFalse()
	{
		ukda = false;
	}
	
	public void printItem()
	{
		common.Utility.log.debug(type + " " + label + " " + namespace + " " + property);
	}
}	
