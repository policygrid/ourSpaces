package common;

/**
 * Designed to return a membership of some sort of group
 * 
 * @author richard
 *
 */

public class MemberBean {
	
	public int userid;
	public String role;
	public String name;
	public String foafID;
	
	public MemberBean()
	{
		
	}
	
	public String getFoafID()
	{
		return foafID;
	}
	
	public void setFoafID(String foafID)
	{
		this.foafID = foafID;
	}
	
	public int getUserID()
	{
		return userid;
	}
	
	public void setUserID(int userid)
	{
		this.userid = userid;
	}
	
	public String getRole()
	{
		return role;
	}
	
	public void setRole(String role)
	{
		this.role = role;
	}
	
	public String getName(){
		return name;
	}
	
	public void setName(String name)
	{
		this.name = name;
	}

}
